async function insertSymptoms() {
    // Create a MongoDB client
    const client = new MongoClient(uri, { useNewUrlParser: true, useUnifiedTopology: true });

    try {
        // Connect to the MongoDB server
        await client.connect();

        // Access the database
        const db = client.db(dbName);

        // Access the symptoms collection
        const collection = db.collection(collectionName);

        // Insert the symptoms object into the collection
        const result = await collection.insertOne(symptoms_json);
        console.log(`${result.insertedCount} symptoms inserted successfully.`);
    } catch (error) {
        console.error('Error inserting symptoms:', error);
    } finally {
        // Close the MongoDB client
        await client.close();
    }
}

